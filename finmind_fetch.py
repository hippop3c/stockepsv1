def fm(dataset, **params):
    params["dataset"] = dataset
    params["token"]   = TOKEN
    r = requests.get(API, params=params, timeout=60)
    r.raise_for_status()
    j = r.json()
    if j.get("status") not in (200, None) and not j.get("data"):
        raise RuntimeError(j.get("msg", "API error"))
    return j.get("data", [])

def cum_to_single(records):
    """累計 EPS → 單季。records 按日期 舊→新。"""
    out = []
    for i, r in enumerate(records):
        yr, mm = r["date"][:4], r["date"][5:7]
        if mm == "03":
            single = r["cum"]
        else:
            prev = records[i-1] if i > 0 else None
            single = (r["cum"] - prev["cum"]) if (prev and prev["date"][:4] == yr) else r["cum"]
        out.append({"date": r["date"], "cum": round(r["cum"], 2),
                    "single": round(single, 2)})
    return out

def main():
    if not TOKEN:
        print("錯誤：找不到 FINMIND_TOKEN 環境變數。")
        sys.exit(1)

    result = {"updated": datetime.date.today().isoformat(),
              "industry": {}, "financials": {}}

    # ① 類股（全市場一次，免費可用）
    print("① 類股 TaiwanStockInfo ...")
    try:
        for d in fm("TaiwanStockInfo"):
            if d.get("stock_id") and d.get("industry_category"):
                result["industry"][d["stock_id"]] = d["industry_category"]
        print(f"   → {len(result['industry'])} 檔產業別")
    except Exception as e:
        print(f"   ⚠ 類股抓取失敗（略過）: {e}")

    # ② 財報 EPS（逐檔，帶 data_id，限速）
    start = f"{datetime.date.today().year - YEARS}-01-01"
    total = len(STOCK_CODES)
    print(f"② 逐檔抓財報 EPS（{total} 檔，start_date={start}，每筆 {SLEEP}s）...")
    print(f"   預估耗時約 {total*SLEEP/60:.0f} 分鐘")
    fin, errs = {}, []
    for i, code in enumerate(STOCK_CODES):
        try:
            rows = fm("TaiwanStockFinancialStatements", data_id=code, start_date=start)
            recs = sorted(
                [{"date": r["date"], "cum": float(r["value"])}
                 for r in rows if r.get("type") == "EPS"],
                key=lambda x: x["date"])
            if recs:
                fin[code] = cum_to_single(recs)
        except Exception as e:
            errs.append(code)
        if (i + 1) % 50 == 0:
            print(f"   進度 {i+1}/{total}，成功 {len(fin)}，失敗 {len(errs)}")
        time.sleep(SLEEP)

    result["financials"] = fin
    print(f"   → 完成：{len(fin)} 檔有財報，{len(errs)} 檔失敗")
    if errs:
        print(f"   失敗代號（前20）: {errs[:20]}")

    with open(OUT, "w", encoding="utf-8") as f:
        json.dump(result, f, ensure_ascii=False, separators=(",", ":"))
    print(f"\n✅ 完成！輸出 {OUT}：類股 {len(result['industry'])}、財報 {len(fin)} 檔")

if __name__ == "__main__":
    main()
